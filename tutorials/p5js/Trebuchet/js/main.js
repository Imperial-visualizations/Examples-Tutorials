class Trebuchet{
    constructor(position = 0,mass){
        this.position = position;
        this.mass = mass;
        this.angle = 0;
    }
    draw(){
        fill(255,0,0);
        beginShape();
        vertex(this.position,450);
        vertex(this.position +50,450);
        vertex(this.position +40,400);
        vertex(this.position + 10,400);
        vertex(this.position,450);
        endShape();
        rect(this.position-25,397,100,5);
    }
}

let trebuchet = new Trebuchet(50,0);

function setup(){
    let a = createCanvas(500,500);
    a.parent('p5canvas');
    trebuchet.draw();
    fill(0,255,0);
    strokeWeight(0);
    rect(0,450,500,50);
}

